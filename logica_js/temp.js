// 1. Declarar a variável com a temperatura em Celsius
let celsius = 32;

// 2. Converter para Fahrenheit usando a fórmula (c * 9/5) + 32
// O JavaScript segue a ordem matemática: multiplicação, divisão e soma.
let fahrenheit = (celsius * 9 / 5) + 32;

// 3. Exibir o resultado
console.log(`${celsius}°C equivale a ${fahrenheit}°F`);

